"""Profile report formatting utilities for benchmark profiling."""

from __future__ import annotations

import cProfile
import io
import pstats
from pathlib import Path


def format_profile_stats(
    profile_path: Path,
    top_n: int = 20,
    filter_stdlib: bool = True,
) -> str:
    """
    Format profile statistics into human-readable text.

    Extracts the top N slowest functions from a cProfile .prof file,
    sorted by cumulative time. Optionally filters out standard library
    functions to focus on application code.

    Args:
        profile_path: Path to .prof file generated by cProfile
        top_n: Number of top functions to show (default: 20)
        filter_stdlib: Filter out Python stdlib functions (default: True)

    Returns:
        Formatted string with profile statistics

    Raises:
        FileNotFoundError: If profile_path doesn't exist
        Exception: If profile file is corrupted or invalid
    """
    if not profile_path.exists():
        raise FileNotFoundError(f"Profile file not found: {profile_path}")

    # Load profile stats
    stats = pstats.Stats(str(profile_path))

    # Sort by cumulative time (time spent in function + callees)
    stats.sort_stats(pstats.SortKey.CUMULATIVE)

    # Capture output to string
    output = io.StringIO()

    # Print header
    output.write(f"Profile: {profile_path.name}\n")
    output.write(f"{'=' * 80}\n\n")

    # Print top N functions
    output.write(f"Top {top_n} functions by cumulative time:\n\n")

    # Use pstats to format the output
    stats.stream = output

    if filter_stdlib:
        # Filter to show only non-stdlib code
        # Focus on geoparquet_io, duckdb, and user code
        stats.print_stats(
            r"(geoparquet_io|duckdb|pyarrow|__main__|test_)",
            top_n,
        )
    else:
        stats.print_stats(top_n)

    return output.getvalue()


def save_profile_data(profiler: cProfile.Profile, output_path: Path) -> None:
    """
    Save cProfile profiler data to file.

    Creates parent directories if they don't exist.

    Args:
        profiler: cProfile.Profile object with collected data
        output_path: Path where .prof file should be written
    """
    # Ensure parent directory exists
    output_path.parent.mkdir(parents=True, exist_ok=True)

    # Save profile stats
    profiler.dump_stats(output_path)
